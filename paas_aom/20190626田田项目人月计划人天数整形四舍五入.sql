SELECT 
I_PRJID,
SUM(I_RLABTLDAY), 
SUM(I_RRANK2+I_RRANK3+I_RRANK4+I_RRANK5+I_RRANK6+I_RRANK7+I_RRANK8+I_RRANK9+I_RRANK10+I_RRANK11+I_RRANK12+I_RRANK13+I_RRANK14)/8
from mdl_aos_prmonthpl
where 1=1
-- and S_PYEARMON <201901
and IS_DELETE = 0
GROUP BY I_PRJID
HAVING SUM(I_RLABTLDAY) != SUM(I_RRANK2+I_RRANK3+I_RRANK4+I_RRANK5+I_RRANK6+I_RRANK7+I_RRANK8+I_RRANK9+I_RRANK10+I_RRANK11+I_RRANK12+I_RRANK13+I_RRANK14)/8